function upsertByField(collectionName, fieldName, docs) {
  const collection = db.getCollection(collectionName);
  docs.forEach((doc) => {
    const query = {};
    query[fieldName] = doc[fieldName];
    collection.replaceOne(query, doc, { upsert: true });
  });
}

